def add_value(list_value: list) -> int:
    symbol = {"#": 5, "O": 3, "X": 1, "!": -1, "!!": -3, "!!!": -5}
    sum = 0
    for List in list_value:
        for Symbol in List:
            sum += symbol[Symbol]
    if sum < 0:
        return 0
    else:
        return sum


